public class Client {
    private String nom;
    private String login;
    private String prenom;

    public Client() {
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getPrenom() {
        return prenom;
    }

    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }

    public Client(String nom, String login, String prenom) {
        this.nom = nom;
        this.login = login;
        this.prenom = prenom;
    }

    
    
}
